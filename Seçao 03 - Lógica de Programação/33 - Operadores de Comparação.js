/**
 * Operadores de Comparação
 * 
 * > = Maior que
 * < = Menor que
 * >= = Maior ou igual a
 * <= = Menor ou igual a
 * == Igual a valor
 * === Igualdade estrita - Igual a valor e tipo
 * !=  Diferente de
 * !== Diferente estrito - Diferente em valor e tipo
 *  
 */
 console.log(10 > 5)
 console.log(10 < 5)
 console.log(10 >= 15)
 console.log(10 <= 15)
 console.log(10 == "10")
 console.log(10 === "10")
 console.log(10 !== "10")

